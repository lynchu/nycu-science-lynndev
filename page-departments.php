            本校於1988年在應用數學系內增聘統計學師資，並於1990年以增設統計組招生。1992年正式成立統計學研究所碩士班；而有鑑於國內統計科學高階研究人才的需求日殷之趨勢，1997年成立博士班。經多年篳路藍縷、努力經營，統計所已打造出優良的師資陣容，研究設備新穎齊全，理論與應用並重。在理論部份，統計所探討新的資料趨勢對於各個次領域統計方法的影響，予以拓展改良，並加強這些方法的理論基礎。在統計應用方面，計畫與工商業界、生物醫學界及政府單位有更多的互動，結合統計所成員的經驗與智慧，提供新且有用的統計方法，以期對產業升級以及政府施政方針均有所貢獻。目前研究領域涵括統計理論與方法、生物統計、工業統計、財務統計及大數據分析。<br><br>
            近年世界的資料量不斷且迅速地增長，巧妙運用這些「巨量資料」(big data)，擷取出其中潛藏的價值，已成為現在最急迫的工作。因應新的科學領域：資料科學(data science)的出現，統計所於 2013 年與資訊工程學系合作成立「巨量資料分析學分學程」，加深同學對巨量資料的認識。接著在 2016 年與資工系及應數系合作成立「數據科學與工程研究所」，使有統計與資工背景之學生能有更多機會，共同學習與交流巨量資料之分析方法。
